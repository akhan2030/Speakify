/**
 * Fisher–Yates shuffle (returns new array)
 * @param {unknown[]} array
 */
function shuffle(array) {
  const copy = [...array];
  for (let i = copy.length - 1; i > 0; i -= 1) {
    const j = Math.floor(Math.random() * (i + 1));
    [copy[i], copy[j]] = [copy[j], copy[i]];
  }
  return copy;
}

/**
 * @returns {typeof PART1_TOPICS[number]}
 */
export function getRandomPart1Topic() {
  const index = Math.floor(Math.random() * PART1_TOPICS.length);
  return PART1_TOPICS[index];
}

/**
 * @param {number} topicId
 * @param {number} [count=4]
 * @returns {string[]}
 */
export function getRandomPart1Questions(topicId, count = 4) {
  const topic = getTopicById(topicId);
  if (!topic) return [];

  const shuffled = shuffle(topic.questions);
  const take = Math.min(count, shuffled.length);
  return shuffled.slice(0, take);
}

/**
 * @param {number[]} [excludeIds=[]]
 * @returns {typeof PART2_CUECARDS[number]}
 */
export function getRandomCueCard(excludeIds = []) {
  const excluded = new Set(excludeIds.map(Number));
  const available = PART2_CUECARDS.filter((card) => !excluded.has(card.id));

  if (available.length > 0) {
    const index = Math.floor(Math.random() * available.length);
    return available[index];
  }

  if (excludeIds.length > 0) {
    const oldestId = Number(excludeIds[0]);
    const leastRecent = getCueCardById(oldestId);
    if (leastRecent) return leastRecent;
  }

  const index = Math.floor(Math.random() * PART2_CUECARDS.length);
  return PART2_CUECARDS[index];
}

/**
 * @param {number} cueCardId
 * @returns {string[]}
 */
export function getPart3Questions(cueCardId) {
  const card = getCueCardById(cueCardId);
  return card?.part3Questions ?? [];
}

/**
 * @param {number} id
 * @returns {typeof PART1_TOPICS[number] | undefined}
 */
export function getTopicById(id) {
  return PART1_TOPICS.find((topic) => topic.id === Number(id));
}

/**
 * @param {number} id
 * @returns {typeof PART2_CUECARDS[number] | undefined}
 */
export function getCueCardById(id) {
  return PART2_CUECARDS.find((card) => card.id === Number(id));
}

/**
 * @param {string} part
 */
export function getQuestionsForPart(part) {
  const key = String(part ?? "").toLowerCase();
  if (key === "part1") {
    return { part: "part1", topics: PART1_TOPICS };
  }
  if (key === "part2") {
    return { part: "part2", cueCards: PART2_CUECARDS };
  }
  if (key === "part3") {
    return { part: "part3", note: "Part 3 questions are linked to Part 2 cue cards" };
  }
  return { part: key, topics: [], cueCards: [] };
}
